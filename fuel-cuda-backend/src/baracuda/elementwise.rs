//! Elementwise unary / binary / ternary / cmp / cast / affine kernels
//! from `baracuda-kernels-sys`. Each kernel becomes a public Rust
//! function with the `(src*, layout*) -> Result<CudaStorageBytes>`
//! shape Fuel's dispatch wrappers expect.
//!
//! ## Contiguous vs strided dispatch
//!
//! Baracuda ships two flavors per kernel:
//! - `<sym>_run(numel, x, y, workspace, workspace_bytes, stream)` —
//!   contiguous fast path. Used when `layout` reports
//!   `is_contiguous_zero_offset` (no strided walk needed).
//! - `<sym>_strided_run(numel, rank, shape, stride_x, stride_y, ...)` —
//!   strided path. Used when the layout isn't contiguous (broadcast,
//!   transpose, view, etc.).
//!
//! The dispatcher picks per-call; Fuel's executor pre-Contiguize pass
//! usually arrives with contig inputs but the strided fallback keeps
//! correctness under all input shapes.
//!
//! ## Macros
//!
//! [`unary_f32`] / [`unary_f16`] / [`unary_bf16`] / [`unary_f64`] are
//! the public entry functions per (kind, dtype) pair. They're
//! generated by `unary_kernel!` from the kernel manifest — adding a
//! new unary kind is one manifest line, not one hand-written wrapper.

