/// Function-pointer aliases for baracuda's unary contig + strided
/// `_run` entry points. Used by the kernel manifest below to bind one
/// FFI symbol per (kind, dtype) pair without re-declaring the FFI
/// shape at each call site.
type UnaryContigRun = unsafe extern "C" fn(
    numel: i64,
    x: *const std::ffi::c_void,
    y: *mut std::ffi::c_void,
    workspace: *mut std::ffi::c_void,
    workspace_bytes: usize,
    stream: *mut std::ffi::c_void,
) -> i32;

type UnaryStridedRun = unsafe extern "C" fn(
    numel: i64,
    rank: i32,
    shape: *const i32,
    stride_x: *const i64,
    stride_y: *const i64,
    x: *const std::ffi::c_void,
    y: *mut std::ffi::c_void,
    workspace: *mut std::ffi::c_void,
    workspace_bytes: usize,
    stream: *mut std::ffi::c_void,
) -> i32;

type WorkspaceSizeFn = unsafe extern "C" fn(numel: i64) -> usize;

/// Core unary-elementwise driver. Handles the contig-vs-strided pick,
/// workspace alloc, FFI launch, status check, and stream sync.
///
/// `op_label` is the static name used in error messages — typically
/// the FFI symbol stem (`"unary_neg_f32"`).
///
/// Allocates a fresh output `CudaStorageBytes` of the same byte count
/// as `src`. Output layout matches input layout's *shape* (which is
/// what Fuel's executor reads back); the output buffer itself is
/// contiguous regardless of input layout.
fn unary_run(
    src: &CudaStorageBytes,
    src_layout: Option<&Layout>,
    contig_run: UnaryContigRun,
    strided_run: UnaryStridedRun,
    workspace_size_fn: Option<WorkspaceSizeFn>,
    op_label: &'static str,
    dtype_size_bytes: usize,
) -> Result<CudaStorageBytes> {
    // When the caller supplies a layout, trust it. When they don't
    // (binding-table callers that haven't migrated to layout-passing),
    // derive a 1-D contiguous layout from the byte buffer's length —
    // matches the existing PTX wrappers' implicit assumption.
    let derived_layout;
    let layout = match src_layout {
        Some(l) => l,
        None => {
            let elems = src.len_bytes() / dtype_size_bytes.max(1);
            derived_layout = Layout::contiguous(Shape::from_dims(&[elems]));
            &derived_layout
        }
    };
    let numel: i64 = layout.shape().elem_count() as i64;
    let out_bytes = (numel as usize) * dtype_size_bytes;
    let device = src.device().clone();
    if out_bytes == 0 {
        return CudaStorageBytes::alloc(&device, 0);
    }
    let out_buf = device.alloc_zeros::<u8>(out_bytes)?;
    let workspace_bytes = match workspace_size_fn {
        Some(f) => unsafe { f(numel) },
        None => 0,
    };
    let scratch = Workspace::alloc(&device, workspace_bytes)?;
    let stream = device.stream().as_raw() as *mut std::ffi::c_void;
    let x_ptr = src.buffer().as_raw().0 as *const std::ffi::c_void;
    let y_ptr = out_buf.as_raw().0 as *mut std::ffi::c_void;

    let contig = is_contiguous_zero_offset(layout);
    let status = if contig {
        // SAFETY: pointers + lengths validated above; stream lives on
        // CudaDevice for the call's duration; workspace is null when
        // workspace_bytes == 0 (per Workspace::alloc contract).
        unsafe {
            contig_run(
                numel,
                x_ptr,
                y_ptr,
                scratch.as_raw(),
                scratch.bytes(),
                stream,
            )
        }
    } else {
        // Strided fallback: pass shape + per-input/output strides
        // (output strides match the contiguous output buffer's layout).
        let out_layout = Layout::contiguous(layout.shape());
        let s = ShapeStridesI32::from_two(layout, &out_layout, op_label)?;
        // SAFETY: shape / stride buffers are owned by `s` for the
        // duration of the call; pointers above.
        unsafe {
            strided_run(
                numel,
                s.rank,
                s.shape.as_ptr(),
                s.stride_x.as_ptr(),
                s.stride_y.as_ptr(),
                x_ptr,
                y_ptr,
                scratch.as_raw(),
                scratch.bytes(),
                stream,
            )
        }
    };
    check(status, op_label)?;
    device.synchronize()?;
    Ok(CudaStorageBytes::from_parts(
        Arc::new(out_buf),
        device,
        out_bytes,
    ))
}

/// True when `layout` represents a contiguous, zero-offset view over
/// its underlying storage — i.e., the storage bytes match the
/// canonical row-major shape order with no stride gaps. Used to pick
/// the contig kernel fast path.
fn is_contiguous_zero_offset(layout: &Layout) -> bool {
    layout.start_offset() == 0 && layout.is_contiguous()
}

/// In-place unary driver. Reuses the same baracuda kernel symbol as
/// the non-inplace cousin but passes the target's pointer for BOTH
/// `x` (read) and `y` (write). Elementwise unary kernels write index
/// `i` after reading index `i` with no cross-thread aliasing, so
/// same-pointer dispatch is safe.
///
/// No strided variant — the executor's `WorkItemKind::InplaceKernel`
/// arm rejects strided targets up front, so we only need the contig
/// kernel path.
fn unary_inplace_run(
    target: &mut CudaStorageBytes,
    contig_run: UnaryContigRun,
    op_label: &'static str,
    dtype_size_bytes: usize,
) -> Result<()> {
    let numel: i64 = (target.len_bytes() / dtype_size_bytes.max(1)) as i64;
    if numel == 0 {
        return Ok(());
    }
    let device = target.device().clone();
    let scratch = Workspace::alloc(&device, 0)?;
    let stream = device.stream().as_raw() as *mut std::ffi::c_void;
    let ptr_mut = target.buffer().as_raw().0 as *mut std::ffi::c_void;
    let ptr_const = ptr_mut as *const std::ffi::c_void;
    // SAFETY: same buffer for x + y is safe for elementwise unary
    // kernels (no cross-thread aliasing); pointers / stream / scratch
    // validated above.
    let status = unsafe {
        contig_run(
            numel,
            ptr_const,
            ptr_mut,
            scratch.as_raw(),
            scratch.bytes(),
            stream,
        )
    };
    check(status, op_label)?;
    device.synchronize()?;
    Ok(())
}

/// Manifest macro for one (kind, dtype) unary entry. Emits a public
/// `pub fn <name>(src: &CudaStorageBytes, layout: &Layout) ->
/// Result<CudaStorageBytes>` that binds the FFI triple
/// `(<sym>_run, <sym>_strided_run, <sym>_workspace_size)` and the
/// dtype's byte size.
///
/// The `$ws` arm is `Some(fn)` when the kernel exposes a workspace
/// query; `None` when its workspace is always zero (most elementwise
/// unary ops).
macro_rules! unary_kernel {
    ($name:ident, $sys_stem:ident, $dtype_size:expr, $op_label:expr $(,)?) => {
        ::paste::paste! {
            #[doc = concat!("Baracuda unary `", $op_label, "` kernel.")]
            pub fn $name(
                src: &CudaStorageBytes,
                layout: Option<&Layout>,
            ) -> Result<CudaStorageBytes> {
                unary_run(
                    src,
                    layout,
                    sys::[<baracuda_kernels_ $sys_stem _run>],
                    sys::[<baracuda_kernels_ $sys_stem _strided_run>],
                    None,
                    $op_label,
                    $dtype_size,
                )
            }
        }
    };
}

